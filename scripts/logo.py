np.random.seed(0)
n = 10000
x = np.random.uniform(-29, 29, n)
y = np.random.uniform(-6, 6, n)
j = ((x + 29) // 1).astype(int)
i = ((y + 6) // 1).astype(int)
x, y = x[mask[i, j]], y[mask[i, j]]

# Generate a set of fibers
mat = Mat(len(x), length=1, width=0.1, thickness=0.05, size=60, tensile=400)
mat.x = x
mat.y = -y
# Build the fiber network
net = Net(mat, periodic=False)
# Stack fibers
stack = Stack(net)
# Create the fiber mesh
mesh = Mesh(stack)
# Instantiate model
model = Timoshenko(mesh)

# Solve the mechanical packing problem
solution = solve(model, packing=4, itermax=10000)

# Export as VTK
msh = vtk_mesh(
    mesh,
    solution.displacement(1),
    solution.rotation(1),
    solution.force(1),
    solution.torque(1),
)
msh.plot(scalars="force", cmap=plt.cm.twilight_shifted)
